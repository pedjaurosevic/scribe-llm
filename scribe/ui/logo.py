# Subtitle that completes the "SCRIBE-LLM" wordmark and states the identity.
SCRIBE_TAGLINE = "L ¬∑ L ¬∑ M   ‚ÄĒ   local-first investigative language agent"

# Compact one-line wordmark for the top bar.
